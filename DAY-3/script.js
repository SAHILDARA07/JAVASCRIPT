// wap to swap(interchange) values of two variables without using third variable.
let a = 20; // a = 20
let b = 30; // b = 30

a = a + b; // a = 20 + 30 = 50
b = a - b; // b = 50 - 30 = 20
a = a - b; // a = 50 - 20 = 30
   
console.log("a =",a);
console.log("b =",b);

